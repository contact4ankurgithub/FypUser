package com.example.fyp.UserModel;

public class ImageModel {

    private String images;

    public String getImages() {
        return images;
    }

    public void setImages(String images) {
        this.images = images;
    }

    @Override
    public String toString() {
        return "ImageModel{" +
                "images='" + images + '\'' +
                '}';
    }
}
